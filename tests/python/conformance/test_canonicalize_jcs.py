"""RFC 8785 JCS canonicalization conformance.

TS same-source
--------------
- TS implementation: ``packages/crypto/src/canonicalization.ts`` (wraps the npm ``canonicalize``)
- Prerequisite for cross-language byte-level wire format consistency

Cross-language alignment contract
---------------------------------
This test does **not** depend on whether a specific TS test file exists; it directly asserts that the
Python ``canonicalize`` output is byte-level correct for several **deterministic RFC 8785 test vectors**:

1. Key order: UTF-16 code unit sorting (equivalent to codepoint sorting within the ASCII range)
2. Strings: standard JSON escaping (quotation / backslash / control char)
3. Numbers: integers have no decimal point; floats use the shortest representation (ECMA-262 ToString equivalent)
4. Arrays: preserve input order
5. fail-closed: bytes / set / NaN / Infinity / circular references → WireSerializationError

Anti self-equal
---------------
Every assert reaches the ``coivitas._wire.canonicalize`` production implementation; inputs are
RFC 8785 standard vectors or vectors reconciled against the TS canonicalize output.
"""

from __future__ import annotations

import math

import pytest

from coivitas._wire import (
    WireSerializationError,
    canonicalize,
    envelope_from_wire,
    envelope_to_wire,
)


class TestCanonicalizeBasicShape:
    """RFC 8785 §3.2.3 key order + §3.2.5 array order preservation."""

    def test_object_keys_sorted_ascii(self) -> None:
        """Object keys are sorted by codepoint (byte-consistent with TS canonicalize)."""
        result = canonicalize({"b": 2, "a": 1, "c": 3})
        # RFC 8785: within ASCII, a < b < c
        assert result == '{"a":1,"b":2,"c":3}'

    def test_array_preserves_order(self) -> None:
        """Arrays preserve input order (no sorting)."""
        result = canonicalize([3, 1, 2])
        assert result == "[3,1,2]"

    def test_nested_object_keys_recursively_sorted(self) -> None:
        """Nested object keys are sorted recursively."""
        result = canonicalize({"z": {"y": 1, "x": 2}, "a": [1, 2, 3]})
        assert result == '{"a":[1,2,3],"z":{"x":2,"y":1}}'

    def test_empty_object_and_array(self) -> None:
        """Minimal output for empty object / empty array."""
        assert canonicalize({}) == "{}"
        assert canonicalize([]) == "[]"


class TestCanonicalizeNumericFormatting:
    """RFC 8785 §3.2.2 + ECMA-262 ToString number formatting."""

    def test_integer_no_decimal_point(self) -> None:
        """Integers: no decimal point (consistent with ECMA-262 ToString)."""
        assert canonicalize({"n": 1}) == '{"n":1}'
        assert canonicalize({"n": 0}) == '{"n":0}'
        assert canonicalize({"n": -42}) == '{"n":-42}'

    def test_integer_float_no_decimal_point(self) -> None:
        """1.0 → "1" (consistent with ECMA-262 ToString; integer floats have no decimal point)."""
        assert canonicalize({"n": 1.0}) == '{"n":1}'
        assert canonicalize({"n": 0.0}) == '{"n":0}'

    def test_negative_zero_normalized(self) -> None:
        """Negative zero: both Python and JS emit "0" (ECMA-262 ToString -0 → "0")."""
        # Python int(-0.0) == 0; consistent with TS behavior
        assert canonicalize({"n": -0.0}) == '{"n":0}'

    def test_nan_and_infinity_fail_closed(self) -> None:
        """NaN / Infinity are not serializable → WireSerializationError."""
        with pytest.raises(WireSerializationError, match="Non-finite"):
            canonicalize({"n": float("nan")})
        with pytest.raises(WireSerializationError, match="Non-finite"):
            canonicalize({"n": float("inf")})
        with pytest.raises(WireSerializationError, match="Non-finite"):
            canonicalize({"n": -math.inf})


class TestCanonicalizeEcma262NumberToString:
    """ECMA-262 §7.1.12.1 ToString(Number) boundary vectors.

    fixture source
    --------------
    All expected values are generated by running the TS-side canonicalize npm package via
    ``node -e "console.log(require('canonicalize')({n: <val>}))"`` at the repo root; not self-equal — the
    Python-side ``_format_number`` must independently implement the ECMA-262 algorithm and match byte-for-byte.

    Boundaries covered
    ------------------
    - epsilon region (1e-7 vs 1e-6): branch 3↔4 boundary (``−6 < n ≤ 0`` upper bound)
    - large integer floats (1e20 vs 1e21): branch 1↔4 boundary (``n ≤ 21`` upper bound)
    - mid precision (1e15 / 1e16): still takes branch 1 (fixed point)
    - double-precision extremes (5e-324 / 1.7976931348623157e+308): branch 4 scientific notation
    - round-trip precision (9.999999999999999e-7 → "0.000001"): repr shortest representation
    """

    @pytest.mark.parametrize(
        "value,expected",
        [
            # epsilon: branch boundary
            (1e-6, "0.000001"),
            (1e-7, "1e-7"),
            (3e-7, "3e-7"),
            # round-trip boundary (Grisu shortest round-trippable → consistent with V8)
            (9.999999999999999e-7, "0.000001"),
            # large integer floats: branch 1↔4 boundary
            (1e15, "1000000000000000"),
            (1e16, "10000000000000000"),
            (1e20, "100000000000000000000"),
            (1e21, "1e+21"),
            (1e22, "1e+22"),
            # standard decimal / integer / integer float
            (0.1, "0.1"),
            (1.5, "1.5"),
            (2.5, "2.5"),
            (100, "100"),
            (100.0, "100"),
            (-1.5, "-1.5"),
            # zero / negative zero (ECMA step 2)
            (0, "0"),
            (-0.0, "0"),
            # double-precision extremes
            (5e-324, "5e-324"),
            (1.7976931348623157e308, "1.7976931348623157e+308"),
        ],
    )
    def test_ecma_262_to_string_byte_equal_to_ts(
        self, value, expected
    ) -> None:
        """Python ``canonicalize`` output is byte-level consistent with TS ``canonicalize({n:value})``.

        The fixture is generated by running the TS canonicalize npm package (i.e. the internal
        dependency of packages/crypto/src/canonicalization.ts) via
        ``node -e "console.log(require('canonicalize')({n:<val>}))"``.
        """
        got = canonicalize({"n": value})
        assert got == f'{{"n":{expected}}}', (
            f"Python canonicalize drift from TS at value={value!r}:\n"
            f"  got     ={got!r}\n  expected={{'n':{expected}}}!r"
        )

    def test_nan_rejected_jcs_does_not_allow(self) -> None:
        """RFC 8785 / JCS forbids NaN."""
        with pytest.raises(WireSerializationError, match="Non-finite"):
            canonicalize({"n": float("nan")})

    def test_positive_infinity_rejected(self) -> None:
        """RFC 8785 / JCS forbids +Infinity."""
        with pytest.raises(WireSerializationError, match="Non-finite"):
            canonicalize({"n": float("inf")})

    def test_negative_infinity_rejected(self) -> None:
        """RFC 8785 / JCS forbids -Infinity."""
        with pytest.raises(WireSerializationError, match="Non-finite"):
            canonicalize({"n": -math.inf})


class TestCanonicalizeUtf16CodeUnitSort:
    """RFC 8785 §3.2.3 UTF-16 code unit lexicographic sorting.

    fixture source
    --------------
    All expected values are generated by running the TS canonicalize npm package via
    ``node -e "console.log(require('canonicalize')({...}))"`` at the repo root; not self-equal.

    Boundaries
    ----------
    - BMP characters (U+0000..U+FFFF): codepoint == UTF-16 code unit, equivalent to the default
      Python sorted
    - supplementary characters (U+10000+): UTF-16 surrogate pair (lead unit 0xD8XX..0xDBXX
      < 0xFFXX high BMP); sorting by codepoint is in the opposite direction of sorting by UTF-16
    - Before the fix, Python ``sorted(keys)`` used codepoint, so the relative order of supplementary
      vs high-BMP characters disagreed with TS; after the fix, using ``utf-16-be`` byte order matches TS.
    """

    def test_bmp_ascii_keys_sorted(self) -> None:
        """ASCII range (including 0x41 'A' < 0x61 'a')."""
        # node verification: `{"A":4,"B":5,"a":1,"b":2,"c":3}`
        assert canonicalize(
            {"b": 2, "a": 1, "c": 3, "A": 4, "B": 5}
        ) == '{"A":4,"B":5,"a":1,"b":2,"c":3}'

    def test_supplementary_plane_before_high_bmp(self) -> None:
        """U+10000 (𐀀, UTF-16 [0xD800,0xDC00]) sorts before U+FFFD (�).

        Python codepoint sorting would place U+10000 later (codepoint 0x10000 > 0xFFFD),
        the opposite of the TS-side JS default sort — UTF-16 code unit sorting is required for byte-level consistency.

        node fixture: `{"𐀀":1,"�":2}` (note 𐀀 comes first)
        """
        keys = {chr(0x10000): 1, chr(0xFFFD): 2}
        got = canonicalize(keys)
        assert got == '{"\U00010000":1,"�":2}', got

    def test_supplementary_pair_with_suffix(self) -> None:
        """supplementary + ASCII suffix vs BMP + ASCII suffix; the lead code unit decides the order.

        node fixture: `{"𐀀b":1,"�a":2}`
        """
        keys = {chr(0x10000) + "b": 1, chr(0xFFFD) + "a": 2}
        got = canonicalize(keys)
        assert got == '{"\U00010000b":1,"�a":2}', got

    def test_emoji_with_bmp_ideograph(self) -> None:
        """U+1F600 😀 is [0xD83D, 0xDE00] in UTF-16; the lead unit 0xD83D < 0xFF21
        (Ａ U+FF21 fullwidth A); Python codepoint sorting would reverse the order.

        node fixture: `{"a":2,"😀":1,"Ａ":3}`
        """
        keys = {chr(0x1F600): 1, "a": 2, "Ａ": 3}
        got = canonicalize(keys)
        assert got == '{"a":2,"\U0001F600":1,"Ａ":3}', got

    def test_chinese_bmp_keys_sorted_consistently(self) -> None:
        """Chinese (BMP) key order: 'a'(0x61) < '中'(0x4E2D) < '文'(0x6587).

        Within the BMP, codepoint == UTF-16 code unit, so TS / Python are equivalent; this test guards that
        the fix does not break existing BMP behavior.

        node fixture: `{"a":3,"中":1,"文":2}`
        """
        got = canonicalize({"中": 1, "文": 2, "a": 3})
        assert got == '{"a":3,"中":1,"文":2}'


class TestCanonicalizeStringEscaping:
    """RFC 8785 §3.2.1 string escaping (standard JSON)."""

    def test_simple_ascii_string(self) -> None:
        assert canonicalize({"s": "hello"}) == '{"s":"hello"}'

    def test_quotation_escaped(self) -> None:
        assert canonicalize({"s": 'a"b'}) == '{"s":"a\\"b"}'

    def test_backslash_escaped(self) -> None:
        assert canonicalize({"s": "a\\b"}) == '{"s":"a\\\\b"}'

    def test_control_chars_escaped(self) -> None:
        # \n / \t standard JSON escaping (consistent with TS canonicalize)
        assert canonicalize({"s": "a\nb"}) == '{"s":"a\\nb"}'
        assert canonicalize({"s": "a\tb"}) == '{"s":"a\\tb"}'

    def test_unicode_preserved_no_ascii_escape(self) -> None:
        """Non-ASCII Unicode is not escaped to \\uXXXX (consistent with TS canonicalize)."""
        assert canonicalize({"s": "héllo"}) == '{"s":"héllo"}'
        assert canonicalize({"s": "中文"}) == '{"s":"中文"}'


