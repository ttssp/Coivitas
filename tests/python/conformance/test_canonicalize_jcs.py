class TestEnvelopeWireRoundtrip:
    """envelope wire format roundtrip (byte-level consistency prerequisite)."""

    def test_envelope_to_wire_byte_stable(self) -> None:
        """Serializing equivalent envelope dicts is byte-level stable (independent of key insertion order)."""
        env1 = {
            "id": "env-1",
            "specVersion": "0.1.0",
            "messageType": "NEGOTIATION_REQUEST",
            "header": {
                "senderDid": "did:agent:" + "a" * 40,
                "recipientDid": "did:agent:" + "b" * 40,
                "sessionId": None,
            },
            "body": {"action": "INQUIRY", "params": {}},
            "signature": "0" * 128,
            "timestamp": "2026-05-07T12:00:00.000Z",
        }
        env2 = {
            # intentionally reversed key insertion; canonicalize must emit the same bytes
            "timestamp": "2026-05-07T12:00:00.000Z",
            "signature": "0" * 128,
            "body": {"params": {}, "action": "INQUIRY"},
            "header": {
                "sessionId": None,
                "recipientDid": "did:agent:" + "b" * 40,
                "senderDid": "did:agent:" + "a" * 40,
            },
            "messageType": "NEGOTIATION_REQUEST",
            "specVersion": "0.1.0",
            "id": "env-1",
        }
        wire1 = envelope_to_wire(env1)
        wire2 = envelope_to_wire(env2)
        assert wire1 == wire2, (
            f"canonicalize wire bytes drift across key insertion order:\n"
            f"  wire1={wire1!r}\n  wire2={wire2!r}"
        )

    def test_wire_roundtrip_preserves_data(self) -> None:
        """envelope_from_wire(envelope_to_wire(env)) ≡ env (semantically equivalent)."""
        env = {
            "id": "env-roundtrip",
            "specVersion": "0.1.0",
            "messageType": "ERROR",
            "header": {
                "senderDid": "did:agent:" + "f" * 40,
                "recipientDid": "did:agent:" + "0" * 40,
                "sessionId": "sess-001",
            },
            "body": {"code": "INVALID_ENVELOPE", "message": "test"},
            "signature": "1" * 128,
            "timestamp": "2026-05-07T12:00:00.000Z",
        }
        wire = envelope_to_wire(env)
        decoded = envelope_from_wire(wire)
        assert decoded == env, (
            f"roundtrip lost data:\n  in={env}\n  out={decoded}"
        )

    def test_wire_invalid_payload_fails_closed(self) -> None:
        """wire bytes that are non-JSON / non-object → WireSerializationError."""
        with pytest.raises(WireSerializationError):
            envelope_from_wire(b"not-json")
        with pytest.raises(WireSerializationError):
            envelope_from_wire(b'"a string"')
        with pytest.raises(WireSerializationError):
            envelope_from_wire(b"[1, 2, 3]")


# ─── JS Number safe integer range guard ───────────────────────────────────


class TestJsSafeIntegerR3FixF2:
    """Reject integers beyond ±(2^53-1); guards against byte-level interop drift between
    Python arbitrary-precision int and JS Number IEEE-754 double precision.

    Anchor: ``packages/sdk-python/src/coivitas/_wire.py`` ``_check_js_safe_integer``.
    """

    def test_safe_max_boundary_accepted(self) -> None:
        """Positive boundary: 2^53 - 1 = 9007199254740991 (JS Number.MAX_SAFE_INTEGER) is valid."""
        result = canonicalize({"n": 2**53 - 1})
        assert result == '{"n":9007199254740991}'

    def test_safe_min_boundary_accepted(self) -> None:
        """Positive boundary: -(2^53 - 1) is valid."""
        result = canonicalize({"n": -(2**53 - 1)})
        assert result == '{"n":-9007199254740991}'

    def test_max_plus_one_rejected(self) -> None:
        """2^53 = 9007199254740992 → reject (JS Number has begun losing precision)."""
        with pytest.raises(WireSerializationError, match="JS safe range"):
            canonicalize({"n": 2**53})

    def test_max_plus_two_rejected(self) -> None:
        """2^53 + 1 = 9007199254740993 → reject (the classic precision-loss case: JS Number
        cannot distinguish 9007199254740992 from 9007199254740993)."""
        with pytest.raises(WireSerializationError, match="JS safe range"):
            canonicalize({"n": 2**53 + 1})

    def test_min_minus_one_rejected(self) -> None:
        """-(2^53) → reject (outside the negative boundary)."""
        with pytest.raises(WireSerializationError, match="JS safe range"):
            canonicalize({"n": -(2**53)})

    def test_huge_int_rejected(self) -> None:
        """10^20 → reject (far beyond the JS safe range)."""
        with pytest.raises(WireSerializationError, match="JS safe range"):
            canonicalize({"n": 10**20})

    def test_format_number_direct_invocation_rejected(self) -> None:
        """Double defense: _format_number also rejects when invoked directly (protects future refactor paths)."""
        from coivitas._wire import _format_number

        with pytest.raises(WireSerializationError, match="JS safe range"):
            _format_number(2**53 + 1)


# ─── base64url strict padding (={0,2}) ────────────────────────────────────


class TestBase64UrlStrictPaddingR3FixF3:
    """from_base64url tightens padding validation (trailing ``=`` 0-2);
    1:1 with TS ``packages/crypto/src/encoding.ts``
    ``BASE64URL_PATTERN = /^[A-Za-z0-9_-]*={0,2}$/``.

    Before tightening, Python ``rstrip("=")`` leniently accepted pathological inputs such as
    ``"AA==="`` / ``"AA===="`` while the TS side rejected them → byte-level interop drift.
    """

    def test_no_padding_accepted(self) -> None:
        """No padding is valid (``"AA"`` → 1 byte)."""
        from coivitas._wire import from_base64url

        assert from_base64url("AA") == b"\x00"

    def test_one_padding_accepted(self) -> None:
        """1 padding char is valid (``"AA="``)."""
        from coivitas._wire import from_base64url

        assert from_base64url("AA=") == b"\x00"

    def test_two_padding_accepted(self) -> None:
        """2 padding chars are valid (``"AA=="``)."""
        from coivitas._wire import from_base64url

        assert from_base64url("AA==") == b"\x00"

    def test_three_padding_rejected(self) -> None:
        """3 padding chars are rejected (TS BASE64URL_PATTERN ={0,2} upper bound)."""
        from coivitas._wire import from_base64url

        with pytest.raises(WireSerializationError, match="padding"):
            from_base64url("AA===")

    def test_four_padding_rejected(self) -> None:
        """4 padding chars are rejected."""
        from coivitas._wire import from_base64url

        with pytest.raises(WireSerializationError, match="padding"):
            from_base64url("AA====")

    def test_illegal_charset_rejected(self) -> None:
        """Illegal character (``*`` is not in [A-Za-z0-9_-]) → reject."""
        from coivitas._wire import from_base64url

        with pytest.raises(WireSerializationError):
            from_base64url("AA*")

    def test_brand_validator_directly_callable(self) -> None:
        """Type-layer _check_base64url unit-test grounding (the public surface goes through Annotated[Base64Url])."""
        from coivitas._brands import _check_base64url

        # valid
        assert _check_base64url("AA") == "AA"
        assert _check_base64url("AA==") == "AA=="
        assert _check_base64url("") == ""  # empty string is valid (consistent with the TS pattern *)
        # invalid
        with pytest.raises(ValueError, match="base64url"):
            _check_base64url("AA===")
        with pytest.raises(ValueError, match="base64url"):
            _check_base64url("AA*")
