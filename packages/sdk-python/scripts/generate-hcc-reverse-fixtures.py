_HEX_FLIP_MAP: dict[str, str] = {
    "0": "f", "1": "e", "2": "d", "3": "c",
    "4": "b", "5": "a", "6": "9", "7": "8",
    "8": "7", "9": "6", "a": "5", "b": "4",
    "c": "3", "d": "2", "e": "1", "f": "0",
}


def _flip_hex_char(c: str) -> str:
    """One-to-one flip of a hex char (0↔f / 1↔e / ... / 7↔8)."""
    out = _HEX_FLIP_MAP.get(c)
    if out is None:
        raise ValueError(f"_flip_hex_char: unexpected char {c!r}")
    return out


@dataclass
class VectorFixtureOutput:
    id: str
    description: str
    chainIdentity: dict[str, str]
    entries: list[dict[str, Any]]  # HashChainEntry.model_dump()
    expected_concat_preimage_hex_per_position: list[str]
    expected_canonical_payload_hash_per_position: list[str]
    expected_final_hash: str
    expected_verify_outcome: str
    expected_reject_error_substring: str | None = None
    expected_reject_error_code: str | None = None
    tamper_description: str | None = None


def _build_vector_output(spec: VectorSpec) -> VectorFixtureOutput:
    entries: list[HashChainEntry] = []
    expected_preimages: list[str] = []
    expected_hashes: list[str] = []
    prev: HashChainEntry | None = None

    for i, payload in enumerate(spec.payloads):
        # Decide which chainIdentity this entry uses (RV6 mixed-identity switches at the specified position)
        if spec.mixed_identity_at == i and spec.mixed_identity_value is not None:
            used_identity = spec.mixed_identity_value
        else:
            used_identity = spec.chain_identity

        entry = _build_deterministic_entry(
            payload=payload,
            chain_identity=used_identity,
            prev_entry=prev,
            vector_seed=spec.id,
        )

        # Compute preimage hex (cross-lang anchor for the TS consumer)
        identity_jcs = canonicalize_chain_identity(used_identity)
        preimage = concat_preimage(entry.canonicalPayload, identity_jcs)
        # Type boundary: bytes → hex
        preimage_hex = preimage.hex()

        expected_preimages.append(preimage_hex)
        expected_hashes.append(entry.canonicalPayloadHash)
        entries.append(entry)
        prev = entry

    # RV7 tampered hash: replace the specified char of the last entry's canonicalPayloadHash
    # Note: expected_canonical_payload_hash_per_position retains the original correct value
    #    (the TS consumer recomputing from the preimage should arrive at the original value);
    #    entries[-1].canonicalPayloadHash is tampered to the flipped value (TS verify should reject).
    if (
        spec.expected_verify_outcome == "REJECT"
        and spec.tamper_last_hash_char_index is not None
        and len(entries) > 0
    ):
        idx = spec.tamper_last_hash_char_index
        last_entry = entries[-1]
        orig = last_entry.canonicalPayloadHash
        # Support negative indexing (-1 = last char); Python string slicing handles it naturally
        if idx < 0:
            real_idx = len(orig) + idx
        else:
            real_idx = idx
        flipped = orig[:real_idx] + _flip_hex_char(orig[real_idx]) + orig[real_idx + 1:]
        # Verify it is still 64 lowercase hex chars (satisfies the HashChainEntry validator)
        assert len(flipped) == 64
        # Reconstruct the entry (pydantic strict → use model_copy)
        tampered_entry = last_entry.model_copy(update={"canonicalPayloadHash": flipped})
        entries[-1] = tampered_entry
        # expected_canonical_payload_hash_per_position retains the original (pre-tamper) value; not changed

    # entries → dict (model_dump); ensure field order matches TS HashChainEntry (aligned with the TS Producer)
    entry_dicts: list[dict[str, Any]] = []
    for entry in entries:
        # model_dump (mode='python') yields an OrderedDict; pydantic v2 preserves field definition order
        # but to keep JSON serialization cross-lang reproducible, output explicitly in TS HashChainEntry field-definition order
        # TS interface order: entryId, canonicalPayload, canonicalPayloadHash, previousHash,
        #                    chainPosition, chainIdentity, timestamp, hccVersion
        entry_dicts.append({
            "entryId": entry.entryId,
            "canonicalPayload": entry.canonicalPayload,
            "canonicalPayloadHash": entry.canonicalPayloadHash,
            "previousHash": entry.previousHash,
            "chainPosition": entry.chainPosition,
            "chainIdentity": entry.chainIdentity,
            "timestamp": entry.timestamp,
            "hccVersion": entry.hccVersion,
        })

    return VectorFixtureOutput(
        id=spec.id,
        description=spec.description,
        chainIdentity=dict(spec.chain_identity),
        entries=entry_dicts,
        expected_concat_preimage_hex_per_position=expected_preimages,
        expected_canonical_payload_hash_per_position=expected_hashes,
        expected_final_hash=expected_hashes[-1],
        expected_verify_outcome=spec.expected_verify_outcome,
        expected_reject_error_substring=spec.expected_reject_error_substring,
        expected_reject_error_code=spec.expected_reject_error_code,
        tamper_description=spec.tamper_description,
    )


