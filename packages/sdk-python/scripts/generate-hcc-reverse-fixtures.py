def _resolve_tenant_placeholders() -> None:
    """Replace every "__TENANT_<key>__" placeholder in _VECTOR_SPECS with a deterministic UUID v4.

    Each placeholder key is unique (RV2 / RV3 / ... / RV6_A / RV6_B / RV7) → derives a distinct tenant UUID.
    Runs once at module load; must be called before assembling entries (idempotent).
    """
    placeholder_prefix = "__TENANT_"
    placeholder_suffix = "__"
    for spec in _VECTOR_SPECS:
        for identity_dict in [spec.chain_identity, spec.mixed_identity_value]:
            if identity_dict is None:
                continue
            tenant_val = identity_dict.get("tenantId")
            if (
                tenant_val is not None
                and tenant_val.startswith(placeholder_prefix)
                and tenant_val.endswith(placeholder_suffix)
            ):
                key = tenant_val[len(placeholder_prefix):-len(placeholder_suffix)]
                identity_dict["tenantId"] = _deterministic_tenant_id(key)


# Resolve placeholders immediately at module load (ensures _build_vector_output gets the UUIDs when called)
_resolve_tenant_placeholders()


