# ─── Deterministic UUID v4 derivation (avoids the non-determinism of uuid.uuid4) ────────────────────


def _deterministic_uuid_v4(seed: str) -> str:
    """Derive a deterministic UUID v4 from a seed (same algorithm source as TS deterministicEntryId).

    Algorithm:
      1. SHA-256(seed) → 32 byte digest
      2. take the first 16 bytes
      3. byte[6] = (byte[6] & 0x0f) | 0x40   (version 4)
      4. byte[8] = (byte[8] & 0x3f) | 0x80   (variant 10)
      5. format as 8-4-4-4-12 hex

    Output looks like "a1b2c3d4-e5f6-4789-8abc-def012345678"; satisfies the RFC 4122 §4.4 UUID v4 format
    plus the AJV schema "format: uuid" plus the HashChainEntry schema "entryId.pattern".
    """
    # Type boundary: string → UTF-8 bytes → SHA-256 → 16 byte UUID
    digest = hashlib.sha256(seed.encode("utf-8")).digest()
    uuid_bytes = bytearray(digest[:16])
    uuid_bytes[6] = (uuid_bytes[6] & 0x0f) | 0x40
    uuid_bytes[8] = (uuid_bytes[8] & 0x3f) | 0x80
    # Type boundary: bytes → hex → UUID 8-4-4-4-12 format
    hex_str = uuid_bytes.hex()
    return (
        f"{hex_str[0:8]}-{hex_str[8:12]}-{hex_str[12:16]}-"
        f"{hex_str[16:20]}-{hex_str[20:32]}"
    )


def _deterministic_entry_id(seed: str, position: int) -> str:
    """Derive a deterministic entry UUID v4 from (seed, position).

    The seed/position pair is unique (same vector_id + position) → same output UUID.
    """
    return _deterministic_uuid_v4(f"{seed}:{position}")


def _deterministic_tenant_id(seed: str) -> str:
    """Derive a deterministic tenant UUID v4 from a seed.

    The AJV schema (hash-chain-entry-v0.2.schema.json $defs.ChainIdentity.tenantId)
    requires tenantId format=uuid; the Python Producer must use the UUID v4 format
    (TS verifyHashChain runs AJV strict and will reject any non-UUID tenantId).
    seed = vector_id + "/tenant" is unique → same vector yields the same UUID; different vectors yield different UUIDs.
    """
    return _deterministic_uuid_v4(f"{seed}/tenant")


def _deterministic_timestamp(position: int) -> str:
    """Derive an ISO 8601 UTC timestamp from a position; output uses a Z suffix plus millisecond precision.

    Format "2026-05-25T00:00:00.000Z" + position-second offset (offset by 1 day from the TS Producer's 2026-05-24;
    ensures the cross-lang fixture cannot false-pass against each other due to a coincidental timestamp).

    Critical: the output format must be compatible with the TS HashChainEntry schema (AJV date-time format).
    The TS Producer uses `new Date(...).toISOString()`, producing "YYYY-MM-DDTHH:MM:SS.sssZ" (Z suffix + 3-digit milliseconds);
    Python's default `.isoformat()` produces a "+00:00" suffix with no milliseconds, which the AJV schema would reject.
    This function formats manually into the TS-equivalent format.
    """
    base = datetime(2026, 5, 25, 0, 0, 0, tzinfo=timezone.utc)
    ts = base + timedelta(seconds=position)
    # Format manually as "YYYY-MM-DDTHH:MM:SS.sssZ" (aligned with TS Date.toISOString() output)
    # strftime does not directly support milliseconds; manually append ".000Z"
    return ts.strftime("%Y-%m-%dT%H:%M:%S.000Z")


# ─── HashChainEntry construction (fully deterministic; does not use append_hash_chain_entry, which calls uuid.uuid4 + datetime.now) ─


def _build_deterministic_entry(
    payload: dict[str, Any],
    chain_identity: ChainIdentity,
    prev_entry: HashChainEntry | None,
    vector_seed: str,
) -> HashChainEntry:
    """Construct a HashChainEntry from (payload, chainIdentity, prevEntry, vectorSeed).

    Uses the same algorithm as append_hash_chain_entry (preimage = canonicalPayloadBytes || chainIdentityJcsBytes); differences:
      - entryId: deterministically derived (vs uuid.uuid4)
      - timestamp: deterministically derived (vs datetime.now)
      - no prev_entry chainIdentity continuity guard (the caller's RV6 negative vector needs to build a mixed chain)
    """
    # Step 1: payload JCS canonicalize → canonicalPayload string
    canonical_payload_bytes: bytes = _jcs_canonicalize(payload)
    canonical_payload: str = canonical_payload_bytes.decode("utf-8")

    # Step 2: chainIdentity JCS canonicalize → ChainIdentityJcs brand
    chain_identity_jcs = canonicalize_chain_identity(chain_identity)

    # Step 3: preimage = UTF8(canonicalPayload) || chainIdentityJcsBytes
    preimage = concat_preimage(canonical_payload, chain_identity_jcs)

    # Step 4: SHA-256 → canonicalPayloadHash hex (64 lowercase)
    canonical_payload_hash: str = compute_canonical_payload_hash_hex(preimage)

    # Step 5: previousHash (genesis = 64 zeros OR prev.canonicalPayloadHash)
    previous_hash: str = (
        HCC_SENTINEL_HASH if prev_entry is None else prev_entry.canonicalPayloadHash
    )

    # Step 6: chainPosition
    position_num: int = 0 if prev_entry is None else prev_entry.chainPosition + 1

    # Step 7: deterministic entryId + timestamp
    entry_id = _deterministic_entry_id(vector_seed, position_num)
    timestamp = _deterministic_timestamp(position_num)

    return HashChainEntry(
        entryId=entry_id,
        canonicalPayload=canonical_payload,
        canonicalPayloadHash=canonical_payload_hash,
        previousHash=previous_hash,
        chainPosition=position_num,
        chainIdentity=dict(chain_identity),
        timestamp=timestamp,
        hccVersion=HCC_VERSION,
    )


