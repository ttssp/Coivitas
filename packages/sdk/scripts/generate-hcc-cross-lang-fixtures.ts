// ─── Deterministic UUID v4 derivation (counters crypto.randomUUID's non-determinism) ───────────

/**
 * deterministicEntryId — derive a deterministic UUID v4 from (seed, position)
 *
 * Algorithm:
 *   1. SHA-256(seed || ":" || position) → 32 byte digest
 *   2. take the first 16 bytes
 *   3. byte[6] = (byte[6] & 0x0f) | 0x40 (version 4)
 *   4. byte[8] = (byte[8] & 0x3f) | 0x80 (variant 10)
 *   5. format as 8-4-4-4-12 hex
 *
 * Output looks like "a1b2c3d4-e5f6-4789-8abc-def012345678"; passes the toHashChainEntryId factory
 * regex validation (strict UUID v4). Same (seed, position) yields the same output.
 */
function deterministicEntryId(seed: string, position: number): string {
    // boundary: string → UTF-8 bytes → SHA-256 → 16 byte UUID
    const input = `${seed}:${position}`;
    const digest = createHash('sha256').update(input, 'utf-8').digest();
    const uuidBytes = Buffer.from(digest.subarray(0, 16));
    uuidBytes[6] = (uuidBytes[6]! & 0x0f) | 0x40;
    uuidBytes[8] = (uuidBytes[8]! & 0x3f) | 0x80;
    // boundary: bytes → hex → UUID 8-4-4-4-12 format
    const hex = uuidBytes.toString('hex');
    return `${hex.slice(0, 8)}-${hex.slice(8, 12)}-${hex.slice(12, 16)}-${hex.slice(16, 20)}-${hex.slice(20, 32)}`;
}

/**
 * deterministicTimestamp — derive an ISO 8601 UTC timestamp from position
 *
 * Base "2026-05-24T00:00:00.000Z" + a per-position second offset; guarantees a strictly monotonic, non-repeating increase.
 */
function deterministicTimestamp(position: number): Timestamp {
    const base = Date.UTC(2026, 4, 24, 0, 0, 0); // 2026-05-24T00:00:00Z (month 0-indexed)
    const ts = new Date(base + position * 1000).toISOString();
    return ts as Timestamp;
}

// ─── HashChainEntry construction (deterministic throughout; avoids appendHashChainEntry due to its randomUUID) ─

/**
 * buildDeterministicEntry — given (payload, chainIdentity, prevEntry, vectorSeed),
 * construct a HashChainEntry; uses the same algorithm as the write path (canonicalize payload + canonicalize identity
 * + concatPreimage + sha256); ID + timestamp are derived deterministically.
 *
 * Identical algorithm to appendHashChainEntry (preimage = canonicalPayloadBytes || chainIdentityJcsBytes
 * order); the differences are:
 *   - entryId: deterministically derived (vs crypto.randomUUID)
 *   - timestamp: deterministically derived (vs new Date)
 *   - no lastEntry chainIdentity continuity guard (the caller's V6 negative vector needs to construct a mixed chain)
 */
function buildDeterministicEntry(
    payload: Record<string, unknown>,
    chainIdentity: ChainIdentity,
    prevEntry: HashChainEntry | undefined,
    vectorSeed: string,
): HashChainEntry {
    // Step 1: payload JCS canonicalize
    const canonicalPayload = canonicalizeHashChainEntryToString(payload);

    // Step 2: chainIdentity JCS canonicalize → ChainIdentityJcs brand
    const chainIdentityJcsRaw = canonicalizeChainIdentity(chainIdentity);
    const chainIdentityJcs = toChainIdentityJcs(chainIdentityJcsRaw);

    // Step 3: preimage concat (payload bytes || identity bytes)
    const preimage = concatPreimage(canonicalPayload, chainIdentityJcs);

    // Step 4: SHA-256 → canonicalPayloadHash hex (64 lowercase) → brand
    const canonicalPayloadHashHex = computeCanonicalPayloadHashHex(preimage);
    const canonicalPayloadHash = toCanonicalPayloadHash(
        canonicalPayloadHashHex,
    );

    // Step 5: previousHash (genesis = 64 zeros OR prev.canonicalPayloadHash)
    const previousHash =
        prevEntry === undefined
            ? toPreviousHash(GENESIS_PREVIOUS_HASH)
            : toPreviousHash(prevEntry.canonicalPayloadHash);

    // Step 6: chainPosition
    const positionNum =
        prevEntry === undefined ? 0 : prevEntry.chainPosition + 1;
    const chainPosition = toChainPosition(positionNum);

    // Step 7: deterministic entryId + timestamp (distinct from appendHashChainEntry's randomUUID + new Date)
    const entryId = toHashChainEntryId(
        deterministicEntryId(vectorSeed, positionNum),
    );
    const timestamp = deterministicTimestamp(positionNum);
    const hccVersion = toHccVersionString(HCC_VERSION_CURRENT);

    return {
        entryId,
        canonicalPayload,
        canonicalPayloadHash,
        previousHash,
        chainPosition,
        chainIdentity,
        timestamp,
        hccVersion,
    };
}

