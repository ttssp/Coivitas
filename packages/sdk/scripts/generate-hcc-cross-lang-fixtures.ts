// ─── Vector → entries[] + expected[] assembly ─────────────────────────────────

interface VectorFixtureOutput {
    id: string;
    description: string;
    chainIdentity: ChainIdentity;
    entries: HashChainEntry[];
    /** Per-entry concat preimage hex (UTF8 bytes; cross-lang byte-level anchor)*/
    expected_concat_preimage_hex_per_position: string[];
    /** Per-entry canonicalPayloadHash (same value as entry.canonicalPayloadHash; a redundant field so the Python test can verify the hash-computation path independently)*/
    expected_canonical_payload_hash_per_position: string[];
    /** The last entry's canonicalPayloadHash (= entries[-1].canonicalPayloadHash; used as the trusted_checkpoint)*/
    expected_final_hash: string;
    /** PASS / REJECT*/
    expected_verify_outcome: 'PASS' | 'REJECT';
    expected_reject_error_substring?: string;
    expected_reject_error_code?: string;
    tamper_description?: string;
}

function flipHexChar(c: string): string {
    // Simple flip rule: 0↔f, 1↔e, 2↔d, 3↔c, 4↔b, 5↔a, 6↔9, 7↔8 (one-to-one)
    const map: Record<string, string> = {
        '0': 'f',
        '1': 'e',
        '2': 'd',
        '3': 'c',
        '4': 'b',
        '5': 'a',
        '6': '9',
        '7': '8',
        '8': '7',
        '9': '6',
        a: '5',
        b: '4',
        c: '3',
        d: '2',
        e: '1',
        f: '0',
    };
    const out = map[c];
    if (out === undefined) {
        throw new Error(`flipHexChar: unexpected char "${c}"`);
    }
    return out;
}

function buildVectorOutput(spec: VectorSpec): VectorFixtureOutput {
    const entries: HashChainEntry[] = [];
    const expectedPreimages: string[] = [];
    const expectedHashes: string[] = [];

    let prev: HashChainEntry | undefined = undefined;

    for (let i = 0; i < spec.payloads.length; i++) {
        // Decide the chainIdentity used by this entry (V6 mixed-identity switches it at the specified position)
        const usedIdentity =
            spec.mixed_identity_at === i && spec.mixed_identity_value
                ? spec.mixed_identity_value
                : spec.chainIdentity;

        const entry = buildDeterministicEntry(
            spec.payloads[i]!.payload,
            usedIdentity,
            prev,
            spec.id,
        );

        // Compute the preimage hex (used for Python cross-lang verification)
        const identityJcs = canonicalizeChainIdentity(usedIdentity);
        const preimage = concatPreimage(entry.canonicalPayload, identityJcs);
        // boundary: Uint8Array → hex string
        const preimageHex = Array.from(preimage, (b) =>
            b.toString(16).padStart(2, '0'),
        ).join('');

        expectedPreimages.push(preimageHex);
        expectedHashes.push(entry.canonicalPayloadHash);
        entries.push(entry);
        prev = entry;
    }

    // V7 tampered hash: replace the Nth character of the last entry's canonicalPayloadHash
    // Note: expected_canonical_payload_hash_per_position keeps the original correct value (the Python side, recomputing from the preimage, should obtain the original);
    // entries[-1].canonicalPayloadHash is tampered into the flipped value (Python verify should reject because recompute != stored).
    // An additional stored_last_hash_after_tamper field is added so the Python test can compare both directions.
    if (
        spec.expected_verify_outcome === 'REJECT' &&
        spec.tamper_last_hash_char_index !== undefined &&
        entries.length > 0
    ) {
        const idx = spec.tamper_last_hash_char_index;
        const lastEntry = entries[entries.length - 1]!;
        const orig = lastEntry.canonicalPayloadHash;
        const flipped =
            orig.slice(0, idx) + flipHexChar(orig[idx]!) + orig.slice(idx + 1);
        // re-cast via the brand factory (flipped is still 64 lowercase hex, satisfying the pattern)
        const tamperedHash = toCanonicalPayloadHash(flipped);
        const tamperedEntry: HashChainEntry = {
            ...lastEntry,
            canonicalPayloadHash: tamperedHash,
        };
        entries[entries.length - 1] = tamperedEntry;
        // expected_canonical_payload_hash_per_position keeps the original value (the Python preimage→hash verification baseline);
        // entries[i].canonicalPayloadHash is the stored tampered value — verify_hash_chain must reject.
        // (do not change the last entry of expectedHashes — leave it as the "correct" baseline)
    }

    // expected_final_hash semantics: when the chain is valid, the last entry's canonicalPayloadHash (i.e. the preimage recompute result).
    // V1-V5 PASS: entries[-1].canonicalPayloadHash === expectedHashes[-1] (the two are equal).
    // V6 mixed-identity: each entry's hash is itself valid (internally consistent per-entry preimage),
    // it is only the chain-level identity that is inconsistent; expectedHashes[-1] = entries[-1].canonicalPayloadHash.
    // V7 tampered hash: entries[-1].canonicalPayloadHash is the tampered, flipped value,
    // expectedHashes[-1] is the original, correct preimage recompute value (differing by 1 character).
    // This field = "what the last entry's hash should be if the chain were valid".
    return {
        id: spec.id,
        description: spec.description,
        chainIdentity: spec.chainIdentity,
        entries,
        expected_concat_preimage_hex_per_position: expectedPreimages,
        expected_canonical_payload_hash_per_position: expectedHashes,
        expected_final_hash: expectedHashes[expectedHashes.length - 1]!,
        expected_verify_outcome: spec.expected_verify_outcome,
        expected_reject_error_substring: spec.expected_reject_error_substring,
        expected_reject_error_code: spec.expected_reject_error_code,
        tamper_description: spec.tamper_description,
    };
}

function generateHccVectors(): object {
    const vectors = VECTOR_SPECS.map(buildVectorOutput);
    return {
        version: 'v0.2',
        generated_by:
            'packages/sdk/scripts/generate-hcc-cross-lang-fixtures.ts',
        ts_canonicalize_lib: 'canonicalize@2.1.0',
        ts_hash_lib: '@noble/hashes/sha256 (TS HCC v0.2)',
        hcc_version: HCC_VERSION_CURRENT,
        preimage_order:
            'canonicalPayloadBytes || chainIdentityJcsBytes',
        vectors,
    };
}

// ─── Serialize + write / check (same pattern as the existing generate-cross-lang-fixtures.ts) ──

function serializeFixture(data: object): string {
    return JSON.stringify(data, null, 4);
}

function runRegenerate(): void {
    mkdirSync(FIXTURE_OUT_DIR, { recursive: true });
    const out = generateHccVectors();
    writeFileSync(OUT_HCC, serializeFixture(out) + '\n', 'utf-8');
    process.stdout.write(
        `[hcc-fixtures:regenerate] wrote 1 fixture file:\n  ${OUT_HCC}\n` +
            `  vectors: ${VECTOR_SPECS.length}\n` +
            `  hcc_version: ${HCC_VERSION_CURRENT}\n`,
    );
}

function runCheck(): void {
    const expectedContent = serializeFixture(generateHccVectors()) + '\n';

    if (!existsSync(OUT_HCC)) {
        process.stderr.write(
            `[hcc-fixtures:check] FAIL — fixture file missing:\n  ${OUT_HCC}\n` +
                `Run 'pnpm hcc:fixtures:regenerate' and commit the result.\n`,
        );
        process.exit(1);
    }

    const actual = readFileSync(OUT_HCC, 'utf-8');
    if (actual === expectedContent) {
        process.stdout.write(
            '[hcc-fixtures:check] PASS — hcc-vectors.json matches TS output.\n',
        );
        return;
    }

    // line-level diff summary (fail-closed: emit the diff to stderr)
    const actualLines = actual.split('\n');
    const expectedLines = expectedContent.split('\n');
    const maxLen = Math.max(actualLines.length, expectedLines.length);
    const diffLines: string[] = [`DIFF in ${OUT_HCC}:`];
    let diffCount = 0;
    for (let i = 0; i < maxLen && diffCount < 20; i++) {
        const a = actualLines[i] ?? '(missing)';
        const e = expectedLines[i] ?? '(missing)';
        if (a !== e) {
            diffLines.push(`  line ${i + 1}: actual  = ${a}`);
            diffLines.push(`  line ${i + 1}: expected= ${e}`);
            diffCount++;
        }
    }
    if (diffCount >= 20) {
        diffLines.push('  ... (truncated at 20 diff lines)');
    }
    process.stderr.write(
        `[hcc-fixtures:check] FAIL — hcc-vectors.json is stale.\n` +
            `Run 'pnpm hcc:fixtures:regenerate' and commit the updated fixture file.\n\n` +
            diffLines.join('\n') +
            '\n',
    );
    process.exit(1);
}

// ─── Entry point ────────────────────────────────────────────────────

const args = process.argv.slice(2);
if (args.includes('--regenerate')) {
    runRegenerate();
} else if (args.includes('--check')) {
    runCheck();
} else {
    process.stderr.write(
        'Usage: generate-hcc-cross-lang-fixtures.ts --regenerate | --check\n',
    );
    process.exit(1);
}
