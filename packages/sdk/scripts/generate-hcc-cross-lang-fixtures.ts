/**
 * generate-hcc-cross-lang-fixtures.ts — cross-lang HCC fixture pipeline (TS Producer)
 *
 * Role
 * ----
 * TypeScript HCC v0.2 (packages/types/src/hash-chain-canonicalize + packages/crypto/...)
 * is the authoritative producer of the cross-lang golden bytes; this script writes out
 * `tests/fixtures/cross-lang/hcc-vectors.json`, which the Python side
 * (packages/sdk-python/tests/hash_chain_canonicalize/test_hcc_cross_lang_fixture.py)
 * consumes to verify byte-level consistency (jcs / SHA-256 / preimage concat order / recursive hash chaining).
 *
 * Usage
 * ----
 *   pnpm hcc:fixtures:regenerate → --regenerate mode: write out hcc-vectors.json
 *   pnpm hcc:fixtures:check → --check mode: compare the existing fixture against a regeneration; diff → exit 1
 *
 * Constraints
 * ----
 * - fail-closed: in --check mode, exit 1 + emit the diff to stderr when the diff is non-empty
 * - determinism: same input, same output (no dependency on crypto.randomUUID / new Date())
 *   - entryId = "0000aaaa-vvvv-4ppp-8nnn-XXXXXXXXXXXX" derived from a SHA-256 truncation of chainIdentityJcs + position
 *   - timestamp = "2026-05-24T00:00:00.000Z" + a per-position second offset
 * - boundary comments are annotated at bytes / hex / string conversion points
 * - anti self-equal: hcc-vectors.json is generated by TS; the Python side reads expected values from the JSON, it does not recompute them
 *
 * Vector set (7 entries)
 * ----------------
 * - V1: single-entry chain (genesis only); chainIdentity = the standard three atp fields
 * - V2: 3-entry chain; same chainIdentity; recursive hash
 * - V3: 5-entry chain; same chainIdentity; multiple payload shapes
 * - V4: 3-entry chain; canonicalPayload contains Unicode CJK + emoji
 * - V5: 4-entry chain; chainIdentity multi-field combination (namespace-only / all fields)
 * - V6: NEGATIVE — mixed chainIdentity (3-entry; identity B grafted onto chain A); the Python side must reject
 * - V7: NEGATIVE — tampered canonicalPayloadHash (3-entry; 1 character of the last entry's hash hex is altered); the Python side must reject
 *
 * Authoritative sources
 * ------
 * - packages/types/src/hash-chain-canonicalize/types.ts (HashChainEntry interface + brand factory)
 * - packages/crypto/src/hash-chain-canonicalize/canonicalize-chain-identity.ts (chainIdentity JCS)
 * - packages/crypto/src/hash-chain-canonicalize/preimage-helpers.ts (concatPreimage + computeHash)
 * - packages/crypto/src/hash-chain-canonicalize/canonicalize-hash-chain-entry.ts (payload JCS)
 */

