/**
 * MCP Bridge — types + lifecycle interface skeleton
 *
 * This file does **not** implement:
 * - the PoP credential resolver (wholly DEFERRED to a later version)
 * - cross-hop sub-token mint+sign (fail-closed CROSS_HOP_DEFERRED_PHASE6; deferred to a later version)
 * - the idempotency canonical request hash (DEFERRED to a later version)
 * - companion specs references (the relevant spec is not yet published, DEFERRED)
 *
 * This file **implements**:
 * - MCPMessage (the MCP protocol wire payload type)
 * - MCPCallerSubject (PoP-based subject — agentDid / mcpClientId; excludes the 'tokenId' kind)
 * - MCPBridgeError (the error registry)
 * - MCPServerConfig / MCPServerLifecycle (the lifecycle interface)
 *
 * Why it lives in the L4 communication layer (packages/communication):
 * the MCP Bridge is an L4 transport-binding adapter (MCP wire ↔ AP envelope);
 * it sits in the same layer as the HTTP / WebSocket transports; it does not introduce L5 orchestration (orchestration is left for the SDK package to consume as needed).
 */

import type { DID, NegotiationEnvelope } from '@coivitas/types';

// ─── MCP wire payload types ─────────────────────────────────────────────────

// Aligned with @modelcontextprotocol/sdk's JSON-RPC protocol;
// this file only declares the shapes and does not introduce the SDK dependency (the SDK dependency is introduced in the server adapter).

/**
 * MCPClient identifier (per MCP spec — generated by the client itself + persisted to the binding registry at registration)
 *
 * Explicitly bound to the AP `senderDid` via `HolderBindingRegistry`;
 * this type is only a wire-level identifier (binding lookup is implemented by holder-binding-resolver).
 */
export type MCPClientId = string;

/**
 * MCPServer identifier (used for the cross-hop fail-closed decision)
 *
 * Decision logic: `if (nextHopMcpServer !== thisServerId()) return { error: 'CROSS_HOP_DEFERRED_PHASE6' }`
 * This file only introduces the type and does not implement the cross-hop path (fail-closed is implemented by cross-hop-guard).
 */
export type MCPServerId = string;

/**
 * MCP protocol message wire payload (JSON-RPC style)
 *
 * Fields correspond to the MCP spec `tools/call` request:
 * - method: tool name (e.g. `tools/call`)
 * - params: call parameters; the `arguments` substructure holds scope dimensions such as numeric_limit / value / currency
 * - id: JSON-RPC request id
 *
 *  numeric_limit dimension alignment — `params.arguments` substructure fields:
 * - numeric_limit: per-call count cap
 * - value: amount (per-call $)
 * - currency: ISO-4217 string (USD/EUR/...)
 * - timeout / max_iterations: MCP-only; does not affect AP scope (table)
 */
export interface MCPMessage {
    /** JSON-RPC version; MCP defaults to '2.0' */
    jsonrpc: '2.0';
    /** request id (generated by the client; echoed back by the server) */
    id: string | number;
    /** MCP method name (e.g. 'tools/call' / 'tools/list') */
    method: string;
    /** request parameters (the concrete shape varies by method) */
    params?: MCPCallParams;
}

/**
 * The params substructure of MCP `tools/call` (fields corresponding to the ScopeValidator)
 *
 * Fields read by the validation logic:
 * - tool: tool name (for audit / logging)
 * - arguments: call-parameters map
 * - arguments.numeric_limit: aligned with AP `max_per_call`
 * - arguments.value: aligned with AP `max_value_per_call` / `max_total_value`
 * - arguments.currency: AP claim.currency match (top-level guard)
 */
export interface MCPCallParams {
    /** tool name (required by MCP `tools/call`) */
    tool: string;
    /** call-parameters map; the scope-validation fields are in this structure */
    arguments: MCPCallArguments;
}

export interface MCPCallArguments {
    /** per-call count cap */
    numeric_limit?: number;
    /** amount value (validated against max_value_per_call / max_total_value) */
    value?: number;
    /** currency code ISO-4217 (top-level guard; required when max_value_per_call or max_total_value exists) */
    currency?: string;
    /** MCP-only field; does not participate in AP scope validation (recorded for audit but not validated) */
    timeout?: number;
    /** MCP-only field; same as timeout */
    max_iterations?: number;
    /** other tool-specific parameters (passed through, not part of scope validation) */
    [key: string]: unknown;
}

// ─── caller subject (ownership / outbox PoP-based subject) ────────────

// Only PoP-based subjects (agentDid / mcpClientId) are accepted;
// the 'tokenId' kind has been **removed** (tokenId is visible in the envelope/audit = an IDOR-style disclosure risk).

// This type does **not** include a 'tokenId' branch (fail-closed: no partial-acceptance of an auth primitive).

export type MCPCallerSubjectKind = 'agentDid' | 'mcpClientId';

export interface MCPCallerSubject {
    kind: MCPCallerSubjectKind;
    value: string;
}

