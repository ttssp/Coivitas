export type MCPErrorCode = (typeof MCP_ERROR)[keyof typeof MCP_ERROR];

/**
 * MCP Bridge standard error;
 * client-visible fields = `mcp_code` + `message`; `internalCode` is used for audit / log only.
 */
export interface MCPBridgeError {
    /** MCP wire code (user-visible) */
    mcp_code: MCPErrorCode;
    /** internal code (audit / log only; not returned to the MCP client) */
    internal_code: string;
    /** human-readable message; a non-normative wire field */
    message: string;
}

export function makeMcpError(
    mcp_code: MCPErrorCode,
    internal_code: string,
    message: string,
): MCPBridgeError {
    return { mcp_code, internal_code, message };
}

// ─── L4 envelope conversion interface ──────────────────────────

/**
 * MCP → AP envelope conversion interface
 *
 * Handles a single-hop call:
 * - receives the incoming MCP message + envelope
 * - the same-hop path forwards incomingEnvelope
 * - the cross-hop path is fail-closed with CROSS_HOP_DEFERRED_PHASE6
 *
 * See envelope-adapter.ts for the implementation.
 */
export interface MCPEnvelopeAdapter {
    /**
     * MCP message → AP envelope conversion.
     */
    adaptIncoming(
        mcpMessage: MCPMessage,
        incomingEnvelope: NegotiationEnvelope,
    ): Promise<
        | { ok: true; envelope: NegotiationEnvelope }
        | { ok: false; error: MCPBridgeError }
    >;
}

// ─── MCP server lifecycle interface (wired in server-adapter.ts) ─

/**
 * MCP server transport mode (corresponds to the MCP spec wire transport)
 *
 * - 'http': HTTP POST / WebSocket (used for outbox + streaming flows)
 * - 'stdio': stdin/stdout (@modelcontextprotocol/sdk default; used for local agent invocation)
 *
 * After introducing @modelcontextprotocol/sdk, the TransportFactory is responsible for constructing the corresponding transport instance.
 */
export type MCPServerTransportMode = 'http' | 'stdio';

export interface MCPServerConfig {
    /** server identifier (the thisServerId() return value; used for the cross-hop fail-closed decision) */
    serverId: MCPServerId;
    /** server agent DID (the agent's own AP DID; collaborates with the holder binding but is not directly related) */
    serverDid: DID;
    /** transport mode */
    transport: MCPServerTransportMode;
    /** http transport config (used only when transport='http') */
    http?: {
        /** listen address; '127.0.0.1' / '0.0.0.0' / etc. */
        host?: string;
        /** listen port; 0 = OS auto-allocation (recommended for tests) */
        port: number;
    };
    /** server name (a recognizable name exposed to the MCP client; an MCP capabilities field) */
    serverName?: string;
    /** server version; an MCP capabilities field */
    serverVersion?: string;
}

/**
 * server lifecycle state machine
 *
 * created → starting → running → stopping → stopped
 *                              ↘
 *                                error
 *
 * The lifecycle is the infrastructure required for the server implementation.
 */
export type MCPServerLifecycleState =
    | 'created'
    | 'starting'
    | 'running'
    | 'stopping'
    | 'stopped'
    | 'error';

/**
 * MCP server lifecycle interface
 *
 * `start` / `stop` / `state` / `serverId` are the minimal set;
 * introducing the outbox + scope validator will not change this interface (separate adapters).
 */
export interface MCPServerLifecycle {
    /** current state-machine state (read synchronously) */
    readonly state: MCPServerLifecycleState;
    /** config (read-only after construction) */
    readonly config: MCPServerConfig;
    /** start the server; state created → running or error; idempotent (noop while running) */
    start(): Promise<void>;
    /** stop the server; state running → stopped; idempotent (noop when stopped/created) */
    stop(): Promise<void>;
}

/**
 * MCP server tool handler — used to register tools after introducing @modelcontextprotocol/sdk
 *
 * Pairs with the MCP spec `tools/list` + `tools/call`;
 * this file only provides the type; it is implemented in server-adapter.ts after wiring in the SDK.
 */
export type MCPToolHandler = (
    params: MCPCallParams,
) => Promise<
    { ok: true; result: unknown } | { ok: false; error: MCPBridgeError }
>;

export interface MCPToolDescriptor {
    /** tool name (exposed by MCP `tools/list`) */
    name: string;
    /** tool description (exposed by MCP `tools/list`) */
    description?: string;
    /** parameters JSON Schema (exposed by MCP `tools/list`) */
    inputSchema?: Record<string, unknown>;
    /** call handler */
    handler: MCPToolHandler;
}
